<?php

namespace PHPM\Bundle\Entity;

use Doctrine\ORM\Mapping as ORM;

/**
 * PHPM\Bundle\Entity\PlageHoraire
 */
class PlageHoraire
{
    /**
     * @var integer $id
     */
    private $id;

    /**
     * @var datetime $debut
     */
    private $debut;

    /**
     * @var datetime $fin
     */
    private $fin;
    
    /**
    * @ORM\OneToMany(targetEntity="Creneau", mappedBy="plageHoraire")
    */
    protected $creneaux;


    /**
     * Get id
     *
     * @return integer 
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set debut
     *
     * @param datetime $debut
     */
    public function setDebut($debut)
    {
        $this->debut = $debut;
    }

    /**
     * Get debut
     *
     * @return datetime 
     */
    public function getDebut()
    {
        return $this->debut;
    }

    /**
     * Set fin
     *
     * @param datetime $fin
     */
    public function setFin($fin)
    {
        $this->fin = $fin;
    }

    /**
     * Get fin
     *
     * @return datetime 
     */
    public function getFin()
    {
        return $this->fin;
    }
}